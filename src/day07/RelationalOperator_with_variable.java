package day07;

public class RelationalOperator_with_variable {

    public static void main(String[] args) {

        int myNumber = 10;


        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5);
        System.out.println(myNumber >= 8);

        System.out.println(myNumber == 5); // == is used to check for equality AND VERY DIFFERENT FROM SINGLE =
        System.out.println(myNumber != 10); // != is used to check for inequality
        System.out.println(myNumber != 6);
    }
}
