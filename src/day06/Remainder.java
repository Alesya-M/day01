package day06;

public class Remainder {
    public static void main(String[] args) {

        System.out.println( 5.0/2f );
        System.out.println( 5/2 );

        //Remainder
        System.out.println( 5 % 2 );
        //2*2 + 1 = 5

        System.out.println( 99 % 10 );
        //10*9 + = 99

        System.out.println( 100 % 10 );
        //10*10 + 0 = 100

    }
}
